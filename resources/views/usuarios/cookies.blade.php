<h2 class="text-2xl mb-2">1. Introdução</h2>
<p>A Hydrax valoriza a sua privacidade e transparência sobre o uso de cookies. Esta política descreve como usamos cookies e tecnologias similares para melhorar sua experiência, personalizar conteúdos, analisar tráfego e oferecer publicidade relevante. Ao utilizar o site Hydrax, você concorda com esta política.</p>
