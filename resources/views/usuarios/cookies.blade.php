<section>
<h2 class="text-2xl mb-2">2. O que são cookies?</h2>
<p>Cookies são pequenos arquivos de texto armazenados em seu dispositivo quando você acessa um site. Eles permitem que o site se lembre de suas preferências e interações, como idioma selecionado, itens adicionados ao carrinho ou sessão de login ativa.</p>
<p>Além disso, cookies podem ser usados para fins analíticos, publicidade personalizada e segurança do site.</p>
</section>

<section>
<h2 class="text-2xl mb-2">3. Tipos de cookies utilizados</h2>

<h3 class="text-xl font-semibold mt-4">3.1 Cookies essenciais</h3>
<p>Esses cookies são indispensáveis para o funcionamento do site Hydrax. Sem eles, não seria possível navegar por áreas seguras, manter sessões de login ou processar compras.</p>

<h3 class="text-xl font-semibold mt-4">3.2 Cookies de desempenho</h3>
<p>Cookies de desempenho coletam informações sobre como os visitantes utilizam o site, por exemplo:</p>
<ul>
<li>Quais páginas são mais visitadas;</li>
<li>Tempo médio gasto em cada página;</li>
<li>Erros encontrados durante a navegação.</li>
</ul>
<p>Esses dados ajudam a melhorar a experiência do usuário e otimizar o desempenho do site.</p>

<h3 class="text-xl font-semibold mt-4">3.3 Cookies de funcionalidade</h3>
<p>Permitem que o site Hydrax lembre suas preferências e escolhas, proporcionando uma experiência personalizada. Exemplos:</p>
<ul>
<li>Lembrar idioma ou região;</li>
<li>Salvar produtos visualizados recentemente;</li>
<li>Manter preferências de layout ou tema.</li>
</ul>

<h3 class="text-xl font-semibold mt-4">3.4 Cookies de publicidade e marketing</h3>
<p>Utilizados para fornecer anúncios relevantes e medir a eficácia das campanhas. Permitem que parceiros da Hydrax exibam ofertas com base nos seus interesses, tanto dentro do site quanto em outros sites.</p>
<p>Exemplos:</p>
<ul>
<li>Anúncios de produtos visualizados;</li>
<li>Recomendações personalizadas;</li>
<li>Remarketing de promoções.</li>
</ul>
</section>

<section>
<h2 class="text-2xl mb-2">4. Cookies de terceiros</h2>
<p>Alguns cookies são gerenciados por terceiros, como provedores de analytics e publicidade. Esses terceiros podem coletar informações sobre suas visitas e interações, mas não têm acesso a dados confidenciais, como senhas ou dados de pagamento.</p>
<p>Exemplos de terceiros que podem usar cookies:</p>
<ul>
<li>Google Analytics;</li>
<li>Facebook Ads;</li>
<li>Plataformas de marketing de afiliados;</li>
<li>Provedores de redes sociais.</li>
</ul>
</section>

<section>
<h2 class="text-2xl mb-2">5. Como gerenciar cookies</h2>
<p>Você pode controlar a utilização de cookies diretamente no seu navegador, configurando bloqueio, exclusão ou notificação de cookies. As instruções variam de acordo com o navegador:</p>
<ul>
<li>Google Chrome: Configurações > Privacidade e segurança > Cookies;</li>
<li>Mozilla Firefox: Opções > Privacidade & Segurança > Cookies e dados de sites;</li>
<li>Safari: Preferências > Privacidade > Cookies e dados de sites;</li>
<li>Microsoft Edge: Configurações > Privacidade, pesquisa e serviços > Cookies.</li>
</ul>
<p>Bloquear cookies pode afetar algumas funcionalidades do site, como login automático ou carrinho de compras.</p>
</section>

